package Introduction_To_Software_Development;

public class Rectangle { // begin class

    double length;
    double width;

    // No-argument constructor

    public Rectangle() {
        this.length = 1.0;
        this.width = 2.0;
    } // end no-argument constructor

    // Parameterized constructor

    public Rectangle(double length, double width){
        this.length = length;
        this.width = width;
    }// end parameter constructor


}// end class
